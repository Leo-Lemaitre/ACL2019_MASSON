package Model;

public class Pacman {

	private int posX;
	private int posY;
	
	public Pacman() {
		this.posX=5;
		this.posY=5;
	}

	public int getPosX() {
		return posX;
	}

	public void setPosX(int posX) {
		this.posX = posX;
	}

	public int getPosY() {
		return posY;
	}

	public void setPosY(int posY) {
		this.posY = posY;
	}
	
	
}
